# 3.30 (Intro to Data Science: Outliers) In statistics, outliers are values out of the ordinary and possibly way out
# of the ordinary. Sometimes, outliers are simply bad data. In the data science case studies, we’ll see that outliers
# can distort results. Which of the three measures of central tendency we discussed—mean, median and mode—is most
# affected by outliers? Why? Which of these measures are not affected or least affected? Why?

# ANSWER
# Outliers most affect the mean because it incorporates every data point, thus even one extreme outlier can
# substantially alter its value. The median is least affected as it is solely based on the middle value, unaffected
# by extreme values. The mode's susceptibility to outliers depends on their distribution.
